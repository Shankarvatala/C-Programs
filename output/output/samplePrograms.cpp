#include <stdio.h>

int main() {
    int num1 = 10;           // Integer variable
    float num2 = 20.5;       // Float variable
    char character = 'A';    // Character variable

    // Output the values
    printf("Integer: %d\n", num1);
    printf("Float: %.2f\n", num2);
    printf("Character: %c\n", character);

    return 0;
}
